package com.four;

public class TestAdd {
    public static void main(String[] args) {
        int i = 1;
        char j = 'c';
        short k = 5;
        System.out.println(i);
        System.out.println(j);
        System.out.println(k);
        System.out.println("添加加号");
        System.out.println(+i);
        System.out.println(+j);
        System.out.println(+k);
        System.out.println("添加减号");
        System.out.println(-i);
        System.out.println(-j);
        System.out.println(-k);
    }
}
