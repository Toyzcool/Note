package eight;

public class RunSwitch {
    public static void main(String[] args) {
        for (int i = 0 ; i < 5 ; i++) {
            switch (i) {
                case 0:
                    System.out.println(0);
                case 1:
                    System.out.println(1);
                case 2:
                    System.out.println(2);
                case 3:
                    System.out.println(3);
            }
        }
    }
}
